package ttu.rakarh1.backend.model.data ;
import java.util.*;

public class ProductCatalog  {
	private int product_catalog ;
	private String name ;
	private String description ;
	private int upper_catalog ;
	private int level ;
    private List<ProductCatalog> subCatalogs ;
    boolean isAbtract;

	public boolean isAbtract()
	{
		return isAbtract;
	}

	public void setAbtract(boolean isAbtract)
	{
		this.isAbtract = isAbtract;
	}

	public ProductCatalog()
	{

	}

	public void setProduct_catalog (int product_catalog)
	{
		this.product_catalog = product_catalog;
	}

	public void setName(String name)
	{
		this.name = name;
	}

	public void setDescription(String description)
	{
		this.description = description;
	}



	public void setUpper_catalog(int upper_catalog)
	{
		this.upper_catalog = upper_catalog;
	}

	public void setLevel(int level)
	{
		this.level = level;
	}

	public void setSubCatalogs (List<ProductCatalog> subcatalogs)
	{
		this.subCatalogs = subcatalogs;
	}



	public int getProduct_catalog ()
	{
		return this.product_catalog ;
	}

	public String getName ()
	{
		return this.name ;
	}

	public String getDescription ()
	{
		return this.description ;
	}

	public int getUpper_catalog ()
	{
		return this.upper_catalog ;
	}

	public int getLevel ()
	{
		return this.level ;
	}

	public List<ProductCatalog> getSubCatalogs ()
	{
		return this.subCatalogs ;
	}

}
